// JavaScript Document
function Limpiar()
	{
		
			
			var buscarDni = Document.getElementById("txtBuscarDni");
			var CodPost = Document.getElementById("txtCodPostu");
			var dni = Document.getElementById("txtPostNumDoc");
			var nomb = Document.getElementById("txtNomApell");
			var fechCurso = Document.getElementById("txtFechaCurso");
			var obser = Document.getElementById("txtDescOberv");
			var check_observa = Document.getElementById("chbxObservacion");
			
			buscarDni.value = "";
			CodPost.value = "";
			dni.value="";
			nomb.value="";
			fechCurso.value ="";
			obser.value = "";
			check_observa.checked = false;

	}

	
	